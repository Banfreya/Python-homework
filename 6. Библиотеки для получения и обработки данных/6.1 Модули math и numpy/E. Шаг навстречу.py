# –§–æ—Ä–º–∞—Ç –≤–≤–æ–¥–∞
# –í –ø–µ—Ä–≤–æ–π —Å—Ç—Ä–æ–∫–µ –∑–∞–ø–∏—Å–∞–Ω—ã –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã –î–µ–∫–∏: –¥–≤–∞ —Ä–∞—Ü–∏–æ–Ω–∞–ª—å–Ω—ã—Ö —á–∏—Å–ª–∞ ‚Äî x –∏ y
# –í–æ –≤—Ç–æ—Ä–æ–π —Å—Ç—Ä–æ–∫–µ –∑–∞–ø–∏—Å–∞–Ω—ã –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã –ü–æ–ª–∏: –¥–≤–∞ —Ä–∞—Ü–∏–æ–Ω–∞–ª—å–Ω—ã—Ö —á–∏—Å–ª–∞ ‚Äî ùúå –∏ ùúô
#
# –§–æ—Ä–º–∞—Ç –≤—ã–≤–æ–¥–∞
# –û–¥–Ω–æ —á–∏—Å–ª–æ ‚Äî —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ –º–µ–∂–¥—É –î–µ–∫–æ–π –∏ –ü–æ–ª–µ–π.
#
# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ
# –î–µ–∫–∞ –∏ –ü–æ–ª—è –∂–∏–≤—É—Ç –Ω–∞ –æ–¥–Ω–æ–π –ø–ª–æ—Å–∫–æ—Å—Ç–∏ —Å –æ–±—â–∏–º —Ü–µ–Ω—Ç—Ä–æ–º.
#
# ùúô ‚Äî –∏–∑–º–µ—Ä—è–µ—Ç—Å—è –≤ —Ä–∞–¥–∏–∞–Ω–∞—Ö.
import math

deka_x, deka_y = map(float, input().split())
polya_x, polya_y = map(float, input().split())
normal_polya_x = polya_x * math.cos(polya_y)
normal_polya_y = polya_x * math.sin(polya_y)
distance = math.sqrt((normal_polya_x - deka_x) ** 2 + (normal_polya_y - deka_y) ** 2)
print(distance)

